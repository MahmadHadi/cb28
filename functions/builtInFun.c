#include<stdio.h>
#include<math.h>

int main() {

    printf("sqrt = %.2f \n", sqrt(116)); // square root

    printf("cbrt = %.2f \n", cbrt(235)); // cube root

    printf("sin(360) = %.2f \n", sin(360));
    printf("cos(360) = %.2f \n", cos(360));
    printf("tan(360) = %.2f \n", tan(360));

    printf("log(10) %.2f \n", log(10));
    printf("log10(10) %.2f \n", log10(10));
    printf("log2(10) %.2f \n", log2(10));

    printf("absolute = %.2f \n", fabs(-20)); // returs aboslute value

    printf("power = %.2f ", pow(2, 40));  // returs power
    // 2 * 2 * 2 * 2 = 16
}